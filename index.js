//Nesse código irei comparar diferentes tipos de variáveis em JavaScript//

//Variáveis://
let numeroUm = 1;
let stringUm = "1";
let numeroTrinta = 30;
let stringTrinta = "30";
let numeroDez = 10;
let stringDez = "10";

//Funções de comparação//
if (numeroUm == stringUm) {
  console.log(
    "As variáveis numeroUm e stringUm nos mostra os mesmos valores mas tipos diferente"
  );
} else {
  console.log("As variáveis não tem o mesmo valor");
}
if (numeroTrinta == stringTrinta) {
  console.log(
    "As variáveis numeroTrinta e stringTrinta tem os mesmos valores mas tipos diferente"
  );
} else {
  console.log("As variáveis não tem o mesmo valor");
}
if (numeroDez == stringDez) {
  console.log(
    "As variáveis numeroDez e stringDez também tem os mesmos valores mas tipos diferente"
  );
} else {
  console.log("As variáveis não tem o mesmo valor");
}
